int32_t main(int argc, char *argv[])
{
    long long int t;
    cin >> t;
    while (t--)
    {

        long long int n;
        cin >> n;
        vector<long long int> arr(n, 0);
        for (int i = 0; i < n; i++)
            cin >> arr[i];
        long long int result = 0;

        unordered_map<long long int, long long int> m;
        for (auto it : arr)
        {
            m[it]++;
        }

        for (int i = 0; i < n; i++)
        {
            if (arr[i] <= 3)
            {
                m[arr[i]]--;
                continue;
            }
            long long int upar = arr[i];
            long long int niche = arr[i] - 3;
            if (upar % niche != 0)
            {
                m[arr[i]]--;
                continue;
            }
            long long int val = upar / niche;
            if (val == arr[i])
            {
                if (m[val] - 1 > 0)
                {
                    result = result + m[val] - 1;
                }
            }
            else
            {
                if (m[val] > 0)
                {
                    result = result + m[val];
                }
            }

            m[arr[i]]--;
        }

        cout << result << endl;
    }
    return 0;
}
