#include <bits/stdc++.h>
using namespace std;

int32_t main(int argc, char *argv[])
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        if (n & 1)
        {
            cout << "-1" << endl;
        }
        else
        {
            for (int i = n; i >= 1; i--)
            {
                cout << i << " ";
            }
            cout << endl;
        }
    }
    return 0;
}