#include <bits/stdc++.h>
using namespace std;

int32_t main(int argc, char *argv[])
{
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        string s;
        cin >> s;
        bool a = 1;
        int ap = 0, bp = 0;
        for (int i = 0; i < n; i++)
        {
            if (s[i] == 'A' && a == 1)
                ap++;
            if (s[i] == 'A' && a == 0)
                a = 1;
            if (s[i] == 'B' && a == 0)
                bp++;
            if (s[i] == 'B' && a == 1)
                a = 0;
        }
        cout << ap << " " << bp << endl;
    }
    return 0;
}